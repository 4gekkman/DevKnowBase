> Стандартные структуры данных в SPL PHP
  > SPL - стандартная библиотека PHP.
  > Она содержит набор стандартных структур данных

> Какие структуры данных есть в SPL?
  > Вот их список:
    - SplDoublyLinkedList   | Двусвязный Список
    - SplStack              | Стек
    - SplQueue              | Очередь
    - SplHeap               | Куча
    - SplMaxHeap            | Двоичная Куча, Пирамида, Сортирующее Дерево
    - SplMinHeap            | Двоичная Куча, Пирамида, Сортирующее Дерево
    - SplPriorityQueue      | Приоритетная очередь
    - SplFixedArray         | Массив фикс.длины и с int индексами
    - SplObjectStorage      | Хранилище объектов

> SplObjectStorage
  > Этот класс имеет двойное предназначание:
    - Либо связывает объекты с данными.
    - Либо, игнорируя данные, представляет набор объектов.






