Справочник по методам класса SplObjectStorage
(подробную спецификацию и примеры см.:   http://ca2.php.net/manual/en/class.splobjectstorage.php  )
  > Класс SplObjectStorage  | хранение объектов
    > addAll          | добавить все объекты из другого хранилища
    > attach          | добавить объект в хранилище
    > contains        | проверить, содержит ли хранилище указанный объект
    > count           | возвращает число объектов в хранилище
    > detach          | удаляет объект из хранилища
    > removeAll       | удалить все объекты, которые есть в другом хранилище, из текущего хранилища
    > removeAllExcept | удалить все объекты, кроме тех, которые есть в другом хранилище

    > current         | возвращает объект на текущей позиции итератора
    > key             | возвращает индекс, на котором сейчас находится итератор
    > next            | передвинуть итератор на следующий объект в хранилище
    > rewind          | передвинуть итератор на предыдущий объект в хранилище

    > offsetExists    | проверить, есть ли указанный объект в хранилище
    > offsetGet       | возвращает данные, ассоциированные с указанным объектом в хранилище
    > offsetSet       | ассоциирует указанные данные с указанным объектом в хранилище
    > offsetUnset     | удаляет объект из хранилища

    > serialize       | сериализовать хранилище, вернуть его строковое представление
    > unserialize     | десериализовать хранилище, и добавить его содержимое к текущему хранилищу
    > getInfo         | возвращает данные, связанные с текущим положением итератора
    > setInfo         | ассоциировать информацию с объектом, на котором сейчас итератор
    > getHash         | возвращает уникальный ID для указанного объекта в хранилище
    > valid           | проверить, валиден ли объект под текущим положением итератора






