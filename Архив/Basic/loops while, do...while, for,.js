
// info
// => у while() условие проверяется до выполнения тела
// => у do...while() условие проверяется после выполнения тела


// цикл while
var i = 0;
while(i<10) {
    i++;
}
while(i++ < 10) {
    
}

// цикл do...while
i = 0;
do {
    i++;
} while(i<10);

// цикл for
for(i=0; i<10; i++) {

}
for(;;) {  // любая часть for может быть пропущена

}
